// Aritmetic operators

// number + number -> number
console.log(1 + 1); // 2

// string + string -> string
console.log("Hello " + "students");

// number - number -> number
console.log(1 - 1); // 0

// number / number -> number
console.log(1 / 1); // 1

// number * number -> number
console.log(1 * 1); // 1

// number % number -> number
console.log(17 % 5); // 2


// Relational operators
// number > number -> boolean
console.log('>', 1 > 2);

// number < number -> boolean
console.log('<', 1 < 2);

// number >= number -> boolean
console.log('>=', 1 >= 2);

// number <= number -> boolean
console.log('<=', 1 <= 2);

// number == number -> boolean
console.log('==', 1 == 2);


